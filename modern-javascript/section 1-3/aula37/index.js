// for
let nome = 'Thito Ribeiro';
for (let i = 0; i < nome.length; i++){
    console.log(nome[i]);
}

// for in
let outroNome = 'Thito Ribeiro';
for (let i in outroNome) {
    console.log(outroNome[i]);
}

// for of
let maisUmNome = 'Thito Ribeiro';
for (let valor of maisUmNome) {
    console.log(valor);
}
