let velocity = 110

switch(velocity) {
    case 80:
        console.log('Velocidade mínima')
        break
    case 90:
        console.log('Velocidade 90km/s')
        break
    case 100:
        console.log('Velocidade aceitável')
        break
    case 110:
        console.log('Velocidade alta, mas aceitável')
        break
    default:
        console.log('Velocidade não identificada')
}