let a = 10;
const b = "20"; 

console.log(a = 10); /* "=" de atribuição */
console.log(a == b); /* "==" comparação de valor */
console.log(a === b); /* "===" comparação de valor e tipo de dado */
console.log(a !== b); /* "!==" comparação se valor e tipo é diferente */
console.log(a != b); /* "!=" comparação se valor */
console.log(a == b && typeof a == 'string'); /* "==" e "&&" (and) comparação com duas condições verdadeiras */
console.log(a == b || typeof b == 'string'); /* "==" e "||" (or) comparação com uma condição falsa */
